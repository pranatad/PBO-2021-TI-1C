    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblPenyewaan.getModel();
        int row = tblPenyewaan.getSelectedRow();

        Penyewaan sewa = new Penyewaan().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        sewa.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

    private void tblPenyewaanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPenyewaanMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblPenyewaan.getModel();
        int row = tblPenyewaan.getSelectedRow();
        Penyewaan sewa = new Penyewaan();

        sewa = sewa.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));

        txtId.setText(String.valueOf(sewa.getIdpenyewaan()));
        cmbCustomer.getModel().setSelectedItem(sewa.getCustomer());
        cmbMovie.getModel().setSelectedItem(sewa.getMovie());
        //txtIdCustomer.setText(String.valueOf(sewa.getCustomer().getIdcustomer()));
        //txtIdMovie.setText(String.valueOf(sewa.getMovie().getIdmovie()));
        txtLamaSewa.setText(String.valueOf(sewa.getLamasewa()));
        
    }//GEN-LAST:event_tblPenyewaanMouseClicked

