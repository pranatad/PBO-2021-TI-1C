    public void tampilkanData() {
        String[] kolom = {"ID", "Customer", "Nama Movie", "Lama Sewa", "Harga Movie", "Total Harga"};
        ArrayList<Penyewaan> list = new Penyewaan().getAll();
        Object rowData[] = new Object[6];

        tblPenyewaan.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        for (Penyewaan sw : list) {
            rowData[0] = sw.getIdpenyewaan();
            rowData[1] = sw.getCustomer().getNamaCustomer();
            rowData[2] = sw.getMovie().getNama();
            rowData[3] = sw.getLamasewa();
            rowData[4] = sw.getMovie().getHarga();
            rowData[5] = sw.getTotalharga();

            ((DefaultTableModel) tblPenyewaan.getModel()).addRow(rowData);
        }
    }

    public void cari(String keyword) {
        String[] kolom = {"ID", "Customer", "Nama Movie", "Lama Sewa", "Harga Movie", "Total Harga"};
        ArrayList<Penyewaan> list = new Penyewaan().search(keyword);
        Object rowData[] = new Object[6];

        tblPenyewaan.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        for (Penyewaan sw : list) {
            rowData[0] = sw.getIdpenyewaan();
            rowData[1] = sw.getCustomer().getNamaCustomer();
            rowData[2] = sw.getMovie().getNama();
            rowData[3] = sw.getLamasewa();
            rowData[4] = sw.getMovie().getHarga();
            rowData[5] = sw.getTotalharga();

            ((DefaultTableModel) tblPenyewaan.getModel()).addRow(rowData);
        }
    }
