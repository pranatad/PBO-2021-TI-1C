    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Peminjaman pjm = new Peminjaman();
        pjm.setIdpeminjaman(Integer.parseInt(txtId.getText()));
        pjm.setTanggalPinjam(Date.valueOf(txtPinjam.getText()));
        pjm.setTanggalKembali(Date.valueOf(txtKembali.getText()));
        pjm.save();
        txtId.setText(Integer.toString(pjm.getIdpeminjaman()));
        tampilkanData();

    }//GEN-LAST:event_btnSimpanActionPerformed

