    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblPeminjaman.getModel();
        int row = tblPeminjaman.getSelectedRow();
        
        Peminjaman pinjam = new Peminjaman().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        pinjam.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

    private void tblPeminjamanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPeminjamanMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblPeminjaman.getModel();
        int row = tblPeminjaman.getSelectedRow();
        Peminjaman pinjam = new Peminjaman();
        
        pinjam = pinjam.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        txtId.setText(String.valueOf(pinjam.getIdpeminjaman()));
        txtIdAnggota.setText(pinjam.getAnggota().getNama());
        txtIdBuku.setText(pinjam.getBuku().getJudul());
        txtPinjam.setText(String.valueOf(pinjam.getTanggalPinjam()));
        txtKembali.setText(String.valueOf(pinjam.getTanggalKembali()));
    }//GEN-LAST:event_tblPeminjamanMouseClicked

