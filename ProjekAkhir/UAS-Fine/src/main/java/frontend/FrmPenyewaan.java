    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Penyewaan sw = new Penyewaan();
 
        sw.setIdpenyewaan(Integer.parseInt(txtId.getText()));

        sw.setCustomer((Customer)cmbCustomer.getSelectedItem());
        sw.setMovie((Movie)cmbMovie.getSelectedItem());
        sw.setLamasewa(Integer.parseInt(txtLamaSewa.getText()));
        
        Poster poster = new Poster();
        poster.setMovie((Movie)cmbMovie.getSelectedItem());
        poster.setSewa(sw);
        if(ckTambah.isSelected()){
            poster.tambahPoster();
        }
        sw.setTotalharga(poster.totalPembayaran());
        sw.save();
        
        txtId.setText(Integer.toString(sw.getIdpenyewaan()));
        tampilkanData();

    }//GEN-LAST:event_btnSimpanActionPerformed

